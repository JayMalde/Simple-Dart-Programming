main(List<String> args) {
  //Integer
  var number1 = 12;
  num number2 = 30;
  print(number1);

  //Double
  var d1 = 12.55;
  double d2 = 3.141;
  print(d2);

  //Boolean
  var b1 = true;
  print(b1);

  //Constant :- Does Not Allow to change Value in Runtime
  const pi = 3.14;
  // pi = 3.22;
  print(pi);

  //Final :- Value will be same for full course of Execution
  final num1 = 334;
  // num = 3.22;
  print(num1);

  // Arithmetic Operatore :- +-*/%
  var n1 = 12;
  var n2 = 24;
  print(n1 + n2);
  print(n2 - n1);
  print(n1 * n2);
  print(n2 / n1);
  print(n2 % n1);
}
